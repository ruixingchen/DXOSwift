import UIKit

extension UIFont {
    static let `default`:UIFont = UIFont.systemFont(ofSize: 17)
    static let title:UIFont = UIFont.systemFont(ofSize: 16)
    static let message:UIFont = UIFont.systemFont(ofSize: 13)
    static let mini:UIFont = UIFont.systemFont(ofSize: 12)
}
